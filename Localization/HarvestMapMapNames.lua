Harvest.mapBlacklist = {
    "tamriel/tamriel"
}

function Harvest.blacklistMap(mapName)
    for _, mapFound in pairs(Harvest.mapBlacklist) do
        if mapFound == mapName then
            return true
        end
    end
    return false
end

function Harvest.GetNewMapName(mapName)
    local result = nil  -- No map found yet

    for newMapName, translations in pairs(Harvest.mapSystem) do -- starts with ["greenshade/shroudedhollowarea1_base"] = {"Shrouded Hollow"},
        if newMapName == mapName then  -- The mapName is already a valid texture name no need to look for one
            return mapName
        elseif Harvest.contains(translations, mapName) then -- {"Shrouded Hollow"} was a match
            if result then -- We FOUND some Esohead map name in "{}" so result equals a texturename already, result is no longer nil, result is a string
                return nil -- result was a string above so return nil and halt looking at the rest of the 600 maps
            else
                result = newMapName -- We FOUND an Esohead style map name in "{}" so make result equal the texturename, it wasn't a texturename already
            end
        end
    end
    return result -- 1) Return newMapName because only one Esohead map name matched, there were no duplicates
                  -- 2) return nil because nothing was found, and the mapName was NOT a texturename already
end

function Harvest.hasNewMapName(mapName)
    for newMapName, translations in pairs(Harvest.mapSystem) do
        if newMapName == mapName then
            return true
        end
    end
    return false
end

function Harvest.updateHarvestNodes(type)

    if Harvest.savedVars[type].data == nil then
        return
    end

    local oldData = Harvest.savedVars[type].data
    Harvest.savedVars[type].data = {}
    --if not Harvest.savedVars["nodes"].oldData then
    --    Harvest.savedVars["nodes"].oldData = {}
    --end
    --if not Harvest.savedVars["nodes"].oldMapData then
    --    Harvest.savedVars["nodes"].oldMapData = {}
    --end
    local newMapName
    local oldMapName

    for map, data in pairs(oldData) do
        newMapName = Harvest.GetNewMapName(map)
        if newMapName then
            for profession, nodes in pairs(data) do
                for index, node in pairs(nodes) do
                    for contents, nodeName in ipairs(node[3]) do

                        if (nodeName) == "chest" or (nodeName) == "fish" then
                            Harvest.newMapNameFishChest(nodeName, newMapName, node[1], node[2])
                        else
                            if node[4] == nil then
                                Harvest.newMapNilItemIDHarvest(newMapName, node[1], node[2], profession, nodeName)
                            else -- node[4] which is the ItemID should not be nil at this point
                                Harvest.newMapItemIDHarvest(newMapName, node[1], node[2], profession, nodeName, node[4])
                            end
                        end

                    end
                end
            end
        else -- << New Map Name NOT found
            oldMapName = map
            for profession, nodes in pairs(data) do
                for index, node in pairs(nodes) do
                    for contents, nodeName in ipairs(node[3]) do

                        if (nodeName) == "chest" or (nodeName) == "fish" then
                            Harvest.oldMapNameFishChest(nodeName, oldMapName, node[1], node[2])
                        else
                            if node[4] == nil then
                                Harvest.oldMapNilItemIDHarvest(oldMapName, node[1], node[2], profession, nodeName)
                            else -- node[4] which is the ItemID should not be nil at this point
                                Harvest.oldMapItemIDHarvest(oldMapName, node[1], node[2], profession, nodeName, node[4])
                            end
                        end

                    end
                end
            end
        end

    end
end

